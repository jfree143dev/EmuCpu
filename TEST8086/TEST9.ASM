; DESCRIPTION : DIFFERENT PARTS OF SPEECH ARE ENTERED AND ARE INSERTED INTO
;               A STATEMENT, AND THE STATEMENT IS OUTPUT
; TESTS THE FOLLOWING INSTRUCTIONS : LEA, CALL INTRA SEGMENT, MOV IMMEDIATE TO
;                                    REGISTER, RET, HLT, INT (AND ALL
;                                    INSTRUCTIONS USED BY THE INTERRUPTS
;                                    CALLED)
; INTERRUPTS CALLED : 10,11

SETREG IP = BEGIN
SETREG SP = 5FFF
SETREG FL = FEFF
CR EQU %13
LF EQU %10
ORG 1000
PROMPT1: DB "Enter a verb (present tense) $"
PROMPT2: DB CR,LF,"Enter an adverb $"
PROMPT3: DB CR,LF,"Enter an adjective $"
PROMPT4: DB CR,LF,"Enter another adjective $
MESS1: DB CR,LF,CR,LF
INPUT1: DB BLOCK 14 {0}
MESS2: DB CR,LF,"a "
INPUT2: DB BLOCK 14 {0}
MESS3: DB CR,LF
INPUT3: DB BLOCK 14 {0}
MESS4: DB CR,LF,"Christmas, and a "
INPUT4: DB BLOCK 14 {0}
MESS5: DB CR,LF,"New Year !!!$"
BEGIN: LEA  DX,PROMPT1
       CALL ROUTINE1
       LEA  DX,INPUT1
       CALL ROUTINE2
       LEA  DX,PROMPT2
       CALL ROUTINE1
       LEA  DX,INPUT2
       CALL ROUTINE2
       LEA  DX,PROMPT3
       CALL ROUTINE1
       LEA  DX,INPUT3
       CALL ROUTINE2
       LEA  DX,PROMPT4
       CALL ROUTINE1
       LEA  DX,INPUT4
       CALL ROUTINE2
       LEA  DX,MESS1
       CALL ROUTINE1
       HLT
ROUTINE1:
       INT  10
       RET
ROUTINE2:
       MOV  CX,14
       INT  11
       RET